			<li><b>Panel damage</b> – &nbsp;&nbsp;&nbsp;When a shadow falls on part of a panel, that portion of the panel turns from a conductor into a resistance and starts heating up. That portion of the panel will eventually burn out and the entire panel will have to be replaced. This will not be covered by warranty</li>
