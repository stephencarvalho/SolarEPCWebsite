<!-- Metering Type Modal Structure -->
<div id="metering-type" class="modal modal-fixed-footer">
	<div class="modal-content">
		<blockquote><h4><b>State Choice</b></h4></blockquote>
		<h5><b>1.	Net Metering</b></h5>
		<p>In net metering arrangement, the meter is allowed to operate in both directions. The electricity produced by the PV system  either provides power directly to the loads or is exported to the grid making the meter rotate backwards, reducing the actual number of units consumed as counted by the meter . </p>



		<h5><b>2.	Gross Metering</b></h5>
		<p>This type of metering measures the generation and import of your electricity separately. By metering the total number of solar units of generated and total number of units consumed, this method allows the utility to charge the customers separately for import, generation and net consumption and if required, even at different rates. This is imposed by having two separate meters or using dual metering . However, in this type of configuration, all of the energy generated is exported to the grid and user has no incentive to increase self-consumption.</p>


